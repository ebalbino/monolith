pub struct DigitalButton {
    down: bool,
    pressed: bool,
    released: bool,
}

pub struct AnalogButton {
    threshold: f32,
    value: f32,
    down: bool,
    pressed: bool,
    released: bool,
}

pub struct Mouse {
    position: Vec2,
    delta_position: Vec2,
    wheel: i32,
    delta_wheel: i32,
    left_button: DigitalButton,
    right_button: DigitalButton,
    middle_button: DigitalButton,
}

pub struct Clock {
    ticks: u64,
    nanoseconds: u64,
    microseconds: u64,
    milliseconds: u64,
    seconds: f32,

    delta_ticks: u64,
    delta_nanoseconds: u64,
    delta_microseconds: u64,
    delta_milliseconds: u64,
    delta_seconds: f32,

    initial_ticks: u64,
    ticks_per_second: u64,
}

pub struct Window {
    size: Vec2,
    scale: f32,
    title: String,
    fullscreen: bool,
    vsync: bool,
    resizable: bool,
    cursor: bool,
}

pub type Keyboard = [DigitalButton; Key::KeyCount as usize];
pub type TextBuffer = [char; 256];

pub struct KeyboardBuffer {
    len: usize,
    buffer: TextBuffer,
}

pub struct Input {
    window: Window,
    clock: Clock,
    mouse: Mouse,
    keyboard: Keyboard,
    text_buffer: KeyboardBuffer,
}
